export type Gen3VanillaBaseGame = keyof typeof gen3VanillaAbilityIdsByBase;

export function getGen3VanillaItemName(itemId: number): string | undefined {
  const name = gen3VanillaItemNames[itemId];
  if (!name || name === '(None)' || name === '???') {
    return undefined;
  }
  return name;
}

export function getGen3VanillaMoveName(moveId: number): string | undefined {
  const name = gen3VanillaMoveNames[moveId];
  if (!name || name === '---') {
    return undefined;
  }
  return name;
}

export function getGen3VanillaAbilityName(baseGame: string | undefined, speciesId: number, abilityBit: number): string | undefined {
  const table = gen3VanillaAbilityIdsByBase[toGen3VanillaBaseGame(baseGame)];
  const entry = table[speciesId];
  if (!entry) {
    return undefined;
  }
  const primary = entry[0];
  const secondary = entry[1];
  const abilityId = abilityBit === 1 && secondary && secondary !== primary ? secondary : primary;
  const name = gen3VanillaAbilityNames[abilityId];
  return name && name !== '-' ? name : undefined;
}

function toGen3VanillaBaseGame(baseGame: string | undefined): Gen3VanillaBaseGame {
  if (baseGame === 'E') {
    return 'E';
  }
  if (baseGame === 'FRLG') {
    return 'FRLG';
  }
  return 'RS';
}
