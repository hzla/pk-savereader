export function decodeGen4String(bytes: Uint8Array, offset: number, length: number): string | undefined {
  const chars: string[] = [];
  for (let i = 0; i + 1 < length; i += 2) {
    const value = readU16LE(bytes, offset + i);
    if (value === GEN4_TERMINATOR) {
      break;
    }
    const char = decodeGen4Char(value);
    if (char) {
      chars.push(char);
    }
  }
  return cleanDecodedText(chars.join(""));
}

export function decodeUtf16LEString(bytes: Uint8Array, offset: number, length: number): string | undefined {
  const chars: string[] = [];
  for (let i = 0; i + 1 < length; i += 2) {
    const value = readU16LE(bytes, offset + i);
    if (value === 0 || value === 0xFFFF) {
      break;
    }
    chars.push(normalizePokemonChar(String.fromCharCode(value)));
  }
  return cleanDecodedText(chars.join(""));
}

function decodeGen4Char(value: number): string | undefined {
  if (value >= GEN4_KOR_START && value <= GEN4_KOR_END) {
    return undefined;
  }
  const char = GEN4_INT_CHARS[value];
  if (!char || char === "\uFFFF") {
    return undefined;
  }
  return normalizePokemonChar(char);
}

function normalizePokemonChar(char: string): string {
  if (char === "\u246D") return "\u2642";
  if (char === "\u246E") return "\u2640";
  return char;
}

function cleanDecodedText(text: string): string | undefined {
  const cleaned = text.trim();
  return cleaned || undefined;
}

function readU16LE(bytes: Uint8Array, offset: number): number {
  if (offset < 0 || offset + 1 >= bytes.length) {
    return 0;
  }
  return bytes[offset] | (bytes[offset + 1] << 8);
}
