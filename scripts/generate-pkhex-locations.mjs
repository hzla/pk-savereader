import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const pkhexCoreRoot = process.env.PKHEX_CORE_ROOT;
if (!pkhexCoreRoot) {
  throw new Error("Set PKHEX_CORE_ROOT to the local PKHeX.Core directory before running this generator.");
}
const pkhexRoot = path.resolve(pkhexCoreRoot, "Resources/text/locations");
const outFile = path.join(repoRoot, "src/data/pkhexLocations.ts");

const sources = {
  RS: "gen3/text_rsefrlg_00000_en.txt",
  E: "gen3/text_rsefrlg_00000_en.txt",
  FRLG: "gen3/text_rsefrlg_00000_en.txt",
  DP: "gen4/text_hgss_00000_en.txt",
  Pt: "gen4/text_hgss_00000_en.txt",
  HGSS: "gen4/text_hgss_00000_en.txt",
  BW: "gen5/text_bw2_00000_en.txt",
  BW2: "gen5/text_bw2_00000_en.txt",
  XY: "gen6/text_xy_00000_en.txt",
  ORAS: "gen6/text_xy_00000_en.txt",
  SM: "gen7/text_sm_00000_en.txt",
  USUM: "gen7/text_sm_00000_en.txt"
};

const arrays = {};
for (const [key, relativePath] of Object.entries(sources)) {
  const text = await readFile(path.join(pkhexRoot, relativePath), "utf8");
  arrays[key] = text
    .replace(/^\uFEFF/, "")
    .split(/\r?\n/)
    .map((line) => line.replace(/^\uFEFF/, ""));
}

await mkdir(path.dirname(outFile), { recursive: true });
await writeFile(
  outFile,
  [
    "// Generated by scripts/generate-pkhex-locations.mjs. Do not edit by hand.",
    "export const pkhexLocations: Record<string, string[]> = " + JSON.stringify(arrays, null, 2) + ";",
    ""
  ].join("\n")
);

console.log(`Wrote PKHeX location tables to ${path.relative(repoRoot, outFile)}`);
