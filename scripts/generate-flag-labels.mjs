import { readFile, writeFile, mkdir } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const pkhexRoot = resolve(root, "../calc-analytics/PKHeX/PKHeX.Core/Resources/text/script");
const outFile = resolve(root, "src/data/flagLabels.ts");

const sources = {
  "gen3-emerald": "gen3/flags_e_en.txt",
  "gen3-frlg": "gen3/flags_frlg_en.txt",
  platinum: "gen4/flags_pt_en.txt",
  hgss: "gen4/flags_hgss_en.txt",
  bw: "gen5/flags_bw_en.txt",
  b2w2: "gen5/flags_b2w2_en.txt",
  xy: "gen6/flags_xy_en.txt",
  oras: "gen6/flags_oras_en.txt",
  sm: "gen7/flags_sm_en.txt",
  usum: "gen7/flags_usum_en.txt"
};

function parseFlagFile(text) {
  const labels = {};
  for (const rawLine of text.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith("#")) {
      continue;
    }
    const match = line.match(/^(\d+)\s+(\S)\s+(.+)$/);
    if (!match) {
      continue;
    }
    const [, id, category, label] = match;
    labels[id] = { category, label };
  }
  return labels;
}

const output = {};
for (const [key, relativePath] of Object.entries(sources)) {
  const text = await readFile(resolve(pkhexRoot, relativePath), "utf8");
  output[key] = parseFlagFile(text);
}

await mkdir(dirname(outFile), { recursive: true });
await writeFile(
  outFile,
  `// Generated by scripts/generate-flag-labels.mjs from PKHeX flag resources.\n` +
    `// Do not edit by hand.\n\n` +
    `export interface FlagLabel {\n` +
    `  category: string;\n` +
    `  label: string;\n` +
    `}\n\n` +
    `export const flagLabels = ${JSON.stringify(output, null, 2)} as const satisfies Record<string, Record<string, FlagLabel>>;\n`,
  "utf8"
);
