import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const calcRoot = process.env.DYNAMIC_CALC_ROOT;
if (!calcRoot) {
  throw new Error("Set DYNAMIC_CALC_ROOT to the local Dynamic-Calc-Hgengine directory before running this generator.");
}
const outFile = path.join(repoRoot, "src/legacy/generatedSources.ts");

const files = [
  "js/savereaders/enums.js",
  "js/savereaders/save_constants/g6_locations.js",
  "js/savereaders/save_constants/g7_locations.js",
  "js/savereaders/save_constants/g67_growths.js",
  "js/savereaders/save_constants/g67_items.js",
  "js/savereaders/save_constants/g67_pkhex_names.js",
  "js/savereaders/save_constants/em_imp_constants.js",
  "js/savereaders/save_constants/em_imp_primary_abils.js",
  "js/savereaders/save_constants/em_imp_primary_mons.js",
  "js/savereaders/save_constants/null_constants.js",
  "js/savereaders/save_constants/orders.js",
  "js/savereaders/save_constants/radical_red_constants.js",
  "js/savereaders/save_constants/radical_red_dex_ability_constants.js",
  "js/savereaders/save_constants/radical_red_randomizer_constants.js",
  "js/savereaders/save_constants/scrambled.js",
  "js/savereaders/save_constants/unbound_2_1_constants.js",
  "js/savereaders/savereader_g3.js",
  "js/savereaders/savereader_g6.js",
  "js/savereaders/savereader_g7.js",
  "js/savereaders/savereader_radicalred.js",
  "js/savereaders/savereader_unbound.js",
  "js/savereaders/savereader_null.js",
  "js/savereaders/savereader_pokeemerald.js",
  "js/savereaders/savereader.js"
];

const entries = [];
for (const relativePath of files) {
  const absolutePath = path.join(calcRoot, relativePath);
  const source = await readFile(absolutePath, "utf8");
  entries.push([relativePath, source]);
}

await mkdir(path.dirname(outFile), { recursive: true });
await writeFile(
  outFile,
  [
    "// Generated by scripts/generate-legacy-sources.mjs. Do not edit by hand.",
    "export const legacySources: Record<string, string> = " + JSON.stringify(Object.fromEntries(entries), null, 2) + ";",
    ""
  ].join("\n")
);

console.log(`Wrote ${entries.length} legacy sources to ${path.relative(repoRoot, outFile)}`);
